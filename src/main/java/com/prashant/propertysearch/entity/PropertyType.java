package com.prashant.propertysearch.entity;

/**
 * Represents various types of properties that can be managed within the system.
 * This enumeration is used to categorize properties into predefined types.
 *
 * Types include:
 * - APARTMENT: Denotes a multi-unit residential structure.
 * - HOUSE: Represents a single-family residential dwelling.
 * - VILLA: Indicates a large, luxury property often associated with exclusivity.
 * - LAND: Refers to undeveloped or vacant plots available for use or development.
 * - COMMERCIAL: Pertains to properties designated for business or commercial activities.
 * - INDUSTRIAL: Covers properties used for industrial purposes, such as factories or warehouses.
 * @author prashant
 */
public enum PropertyType {
    APARTMENT,
    HOUSE,
    VILLA,
    LAND,
    COMMERCIAL,
    INDUSTRIAL
}
